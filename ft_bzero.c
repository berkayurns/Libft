#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	ft_memset(s, 0, n);
}
#include <unistd.h>
int	main()
{
	char a[] = "deneme";
	ft_bzero(a, 1);	
write(1, &a, 6);
}