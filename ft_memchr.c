#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)s)[i] == (unsigned char)c)
			return ((void *)(s + i));
		i++;
	}
	return (0);
}
#include <stdio.h>

int main()
{	
	char *s = "Deneme";
	printf("%s\n", (char *)ft_memchr(s,'m',6));
}